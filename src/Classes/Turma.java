public class Turma {
    private String nome;
    private int idTurma;
    private String sala;
    private String turno;
    

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdTurma() {
        return idTurma;
    }

    public void setIdTurma(int idtTurma) {
        this.idTurma = idtTurma;
    }

    public String getSala() {
        return sala;
    }

    public void setSala(String sala) {
        this.sala = sala;
    }

    public String getTurno() {
        return turno;
    }

    public void setTurno(String turno) {
        this.turno = turno;
    }

    

    
    
    
    
    
    
}
