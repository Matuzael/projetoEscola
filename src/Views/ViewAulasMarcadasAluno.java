      public void readJTable(){
         DefaultTableModel modelo = (DefaultTableModel) tabelaAulasMarcadas.getModel();
        modelo.setNumRows(0);
        AulaDAO adao = new AulaDAO();
        
        for(Aula a: adao.read()){
            
            modelo.addRow(new Object[]{
                a.getIdAula(),
                a.getProfessor(),
                a.getDisciplina(),
                a.getTurma(),
                a.getLocal(),
                a.getHora()
            });
            
    }
        
    }
 
    
