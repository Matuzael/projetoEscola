      public void readJTable(){
            DefaultTableModel modelo = (DefaultTableModel) tabelaAulasMarcadasAluno.getModel();
            modelo.setNumRows(0);
            AulaDAO adao = new AulaDAO();
        
        for(Aula a: adao.readAulasMarcadas(txtTurma.getText())){
            
            modelo.addRow(new Object[]{
                a.getIdAula(),
                a.getProfessor(),
                a.getDisciplina(),
                a.getTurma(),
                a.getLocal(),
                a.getHora()
            });
            
    }}
